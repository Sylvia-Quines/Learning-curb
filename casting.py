# Casting - converting a value from one datatype to another

v = int('3')
y = int(8)
h = int(3.89)
b = int(-67)

print(v)
print(y)
print(h)
print(b)

v = float('3')
y = float(8)
h = float(3.89)
b = float(-67)

print(v)
print(y)
print(h)
print(b)

v = str('3')
y = str(8)
h = str(3.89)
b = str(-67)

print(v)
print(y)
print(h)
print(b)


